export function renderModulesMd(opts: { scan: ProjectScan }): string {
  const dirs = opts.scan.topDirs.map(
    (d) =>
      `### \`${d.name}/\`\n\n` +
      `**Role:** ${d.guessedRole ?? "_unclear — please describe_"}\n\n` +
      `**Owns:** _(what does this module own?)_\n\n` +
      `**Does NOT own:** _(what should NEVER live here?)_\n\n` +
      `**Depends on:** _(internal/external dependencies)_\n`,
  );

  return `# Modules

${DRAFT_BANNER}

> Module boundaries — what each part owns and (importantly) does NOT own.
> The "does NOT own" line is the most valuable for AI agents.

${
  dirs.length === 0
    ? "_No top-level directories detected._"
    : dirs.join("\n---\n\n")
}
`;
}

